  'февруари',
  'март',
  'април',
  'май',
  'юни',
  'юли',
  'август',
  'септември',
  'октомври',
  'ноември',
  'декември',
];

/// Напр. „1 юли".
String bgDate(DateTime d) => '${d.day} ${bgMonths[d.month - 1]}';
